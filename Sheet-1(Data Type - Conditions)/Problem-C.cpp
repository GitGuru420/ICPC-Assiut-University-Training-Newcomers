#include <bits/stdc++.h>
using namespace std;

int main() {
    long long x, y;
    cin >> x >> y;

    // summation
    cout << x << " + " << y << " = " << x + y << endl;

    // multiplication
    cout << x << " * " << y << " = " << x * y << endl;

    // subtraction
    cout << x << " - " << y << " = " << x - y << endl;

    return 0;
}